chislo1 = int(input("Введите первое число: "))
action = input("Введите действие: ")
chislo2 = int(input("Введите второе число: "))
if action == "+" :  
    print (chislo1 + chislo2)
if action == "-" :
    print (chislo1 - chislo2)
if action == "*" :
    print (chislo1 * chislo2)
if action == "/" :
    print (chislo1 / chislo2)